package com.zamouli.aiassistant

/**
 * أوقات اليوم المختلفة التي تساعد في تخصيص المحتوى بناءً على الوقت
 */
enum class TimeOfDay {
    EARLY_MORNING,  // الصباح الباكر (من 5 صباحاً إلى 8 صباحاً)
    MORNING,        // الصباح (من 8 صباحاً إلى 12 ظهراً)
    NOON,           // الظهيرة (من 12 ظهراً إلى 2 مساءً)
    AFTERNOON,      // بعد الظهر (من 2 مساءً إلى 5 مساءً)
    EVENING,        // المساء (من 5 مساءً إلى 9 مساءً)
    NIGHT,          // الليل (من 9 مساءً إلى 12 منتصف الليل)
    LATE_NIGHT      // منتصف الليل (من 12 منتصف الليل إلى 5 صباحاً)
}